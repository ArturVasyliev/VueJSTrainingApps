const app = Vue.createApp({
  data() {
    return {
      counter: 0,
      name: ''
    };
  },
  methods: {
    setName(event) {
      this.name = event.target.value;
    },
    add(num) {
      this.counter = this.counter + num;
    },
    reduce(num) {
      this.counter = this.counter - num;
      // this.counter--;
    },
    resetInput() {
      this.name = '';
    },
    outputFullName() {
      console.log('outputFullName call');
      if(this.name === ''){
        return '';
      }
      return this.name + ' ' + 'Vasyliev';
    }
  }
});

app.mount('#events');
